package entites;

import java.io.Serializable;
import java.util.Calendar;
import java.util.Set;

public class RequestForHelp implements Serializable {

    private int id;
    private User authorUser;
    private String name;
    private String type;
    private String region;
    private String district;
    private String city;
    private String street;
    private String houseNumber;
    private Calendar creationDate;
    private Calendar startDate;
    private String description;
    private Set<User> participants;
    private Set<PhotoReport> photoReports;

    public RequestForHelp() {

    }

    public int getId() {
        return id;
    }

    public User getAuthorUser() {
        return authorUser;
    }

    public void setAuthorUser(User authorUser) {
        this.authorUser = authorUser;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getRegion() {
        return region;
    }

    public void setRegion(String region) {
        this.region = region;
    }

    public String getDistrict() {
        return district;
    }

    public void setDistrict(String district) {
        this.district = district;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public String getHouseNumber() {
        return houseNumber;
    }

    public void setHouseNumber(String houseNumber) {
        this.houseNumber = houseNumber;
    }

    public Calendar getCreationDate() {
        return creationDate;
    }

    public void setCreationDate(Calendar creationDate) {
        this.creationDate = creationDate;
    }

    public Calendar getStartDate() {
        return startDate;
    }

    public void setStartDate(Calendar startDate) {
        this.startDate = startDate;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public Set<User> getParticipants() {
        return participants;
    }

    public void setParticipants(Set<User> participants) {
        this.participants = participants;
    }

    public Set<PhotoReport> getPhotoReports() {
        return photoReports;
    }

    public void setPhotoReports(Set<PhotoReport> photoReports) {
        this.photoReports = photoReports;
    }
}
